# The sum() function adds up all values in a list.

numbers = [1, 2, 3, 4, 5]
total = sum(numbers)     # Adds 1 + 2 + 3 + 4 + 5
print(f"Numbers: {numbers}")
print(f"Sum of numbers: {total}")  # Should print 15
