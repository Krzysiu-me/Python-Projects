# Variables store information that can be used later in your program.
# You can change (reassign) variables at any time.

# String (text) variable
name = "Krzysiu"

# Integer (whole number) variable
age = 20

# Float (decimal number) variable
height = 1.83

# Boolean (True or False) variable
is_student = True

# Print all variables
print(f"Name: {name}")
print(f"Age: {age}")
print(f"Height: {height} meters")
print(f"Are you a student? {is_student}")

# Changing variable values
age = 21
print(f"Next year, you will be {age} years old.")

# Type checking
print(type(name))    # <class 'str'>
print(type(age))     # <class 'int'>
print(type(height))  # <class 'float'>
print(type(is_student)) # <class 'bool'>
