# The range() function generates a sequence of numbers, which is useful for looping.

print("Counting from 0 up to 4:")
for i in range(5):
    print(i)   # Prints 0, 1, 2, 3, 4

print("Counting from 2 up to 5 (not including 6):")
for i in range(2, 6):
    print(i)   # Prints 2, 3, 4, 5
