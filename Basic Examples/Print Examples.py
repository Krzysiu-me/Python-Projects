# The print() function displays output in the terminal.

print("Hello, world!")

# Printing multiple values:
name = "Krzysiu"
age = 20
print("Name:", name, "Age:", age)

# Using f-strings for formatted output (recommended in Python 3.6+):
print(f"My name is {name} and I am {age} years old.")

# Printing numbers and calculations:
print("5 + 7 =", 5 + 7)

# Printing with line breaks and tabs:
print("First Line\nSecond Line\t(Indented)")

# Printing special characters:
print("She said, \"Python is awesome!\"")
