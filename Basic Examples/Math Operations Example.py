# These are the basic math operations you can perform in Python.

x = 7
y = 3

print(f"Addition: {x} + {y} = {x + y}")        # Adds x and y
print(f"Subtraction: {x} - {y} = {x - y}")     # Subtracts y from x
print(f"Multiplication: {x} * {y} = {x * y}")  # Multiplies x by y
print(f"Division: {x} / {y} = {x / y}")        # Divides x by y (result is a float)
print(f"Integer Division: {x} // {y} = {x // y}") # Divides and rounds down to whole number
print(f"Modulus: {x} % {y} = {x % y}")         # Remainder after division
print(f"Exponentiation: {x} ** {y} = {x ** y}") # x raised to the power of y
