# Strings have many useful methods you can use.

text = "Hello, World!"

print(text.lower())           # Converts all characters to lowercase
print(text.upper())           # Converts all characters to uppercase
print(text.replace("Hello", "Hi")) # Replaces "Hello" with "Hi"
print(f"Length of the string: {len(text)}")    # Counts all characters including spaces and punctuation
