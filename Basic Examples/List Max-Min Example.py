# You can find the largest and smallest values in a list with max() and min().

numbers = [10, 4, 7, 1, 13]
print(f"Numbers: {numbers}")
print(f"Maximum value: {max(numbers)}")  # Returns 13
print(f"Minimum value: {min(numbers)}")  # Returns 1
